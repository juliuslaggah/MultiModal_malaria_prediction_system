import os
from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from fastapi.staticfiles import StaticFiles
from . import models, database, api

# ==========================================
# 1. DATABASE INITIALIZATION
# ==========================================
# Creates the PostgreSQL tables if they don't exist
models.Base.metadata.create_all(bind=database.engine)

# ==========================================
# 2. APP SETUP
# ==========================================
app = FastAPI(
    title="Malaria Multimodal Detection System",
    description="Thesis Project: Late Fusion of Clinical and Image Data with Explainability",
    version="1.0"
)

# ==========================================
# 3. STATIC FILES (For Explainability)
# ==========================================
# This is CRITICAL for displaying the Grad-CAM heatmaps generated by ml_service.py
# We determine the absolute path to the 'static' folder inside the 'app' directory
current_dir = os.path.dirname(os.path.abspath(__file__))
static_dir = os.path.join(current_dir, "static")

# Ensure the directory exists to prevent startup errors
os.makedirs(static_dir, exist_ok=True)

# Mount the directory to the URL path "/static"
app.mount("/static", StaticFiles(directory=static_dir), name="static")

# ==========================================
# 4. SECURITY & CORS
# ==========================================
# Allows the Frontend (React/Streamlit) to communicate with this Backend
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],  # For Thesis demo, allow all. In production, restrict this.
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# ==========================================
# 5. ROUTES
# ==========================================
app.include_router(api.router)

@app.get("/")
def read_root():
    """
    Health check endpoint.
    """
    return {
        "status": "System Operational", 
        "model_architecture": "Late Fusion (ShuffleNetV2 + XGBoost)",
        "explainability": "Enabled (SHAP + Grad-CAM)"
    }
